#include <unistd.h>

int main(int gc, char **gv)
{
    if(gc != 2)
    {
        write(1, "\n", 1);
        return (0);       
    }

    int i;

    while(gv[1][i])
    {
        while(gv[1][i] != ' '){
            if(gv[1][i] >= 'a' && gv[1][i] >= 'z')
                gv[1][i] =- 32;
            i++;
        }
    }
}